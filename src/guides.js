export const guidePages = guideTopics.map((topic, index) => {
  const path = `/guides/${topic.slug}/`;
  const sections = buildGuideSections(topic);
  const plainText = sections
    .flatMap((section) => [section.title, ...(section.paragraphs || []), ...(section.items || [])])
    .join("\n");

  return {
    ...topic,
    index: index + 1,
    path,
    metaTitle: `${topic.title} - Ready tool 가이드`,
    description: `${topic.keyword}를 실제 제출 상황, 실패 원인, 검수 기준, Ready tool 활용 순서로 정리한 공익형 실무 가이드입니다.`,
    sections,
    plainText,
    nonSpaceLength: countNonSpace(plainText),
    dateModified: "2026-05-13"
  };
});

export function getGuideByPath(pathname) {
  const normalized = normalizePath(pathname);
  return guidePages.find((guide) => guide.path === normalized) || null;
}

export function normalizePath(pathname) {
  let path = pathname || "/";
  if (!path.startsWith("/")) path = `/${path}`;
  if (path !== "/" && !path.endsWith("/")) path = `${path}/`;
  return path;
}

export function countNonSpace(value) {
  return String(value || "").replace(/\s/g, "").length;
}

